a = input('\033[31mDigite algo: \033[m')
print('\033[34mO tipo primitivo desse valor é\033[m',type(a))
print('\033[34mSó tem espaços?\033[m ',a.isspace())
print('\033[34mé um número?\033[m', a.isnumeric())
print('\033[34mé alfabetico?\033[m ', a.isalpha())
print('\033[34mé alfanumerico?\033[m ', a.isalnum())
print('\033[34mEstá em maiúsculas?\033[m', a.isupper())
print('\033[34mEstá em minúsculas?\033[m ', a.islower())
print('\033[34mEstá captalizada?\033[m ', a.istitle())
